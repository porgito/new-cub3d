#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	unsigned char	element;
	char			*str;

	i = 0;
	element = c;
	str = b;
	while (i < len)
	{
		str[i] = element;
		i++;
	}
	return (b);
}
