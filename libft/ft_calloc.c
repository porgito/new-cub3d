#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*pointer;

	if (size > 0 && count > (SIZE_MAX / size))
		return (NULL);
	pointer = malloc(count * size);
	if (! (pointer))
		return (NULL);
	ft_bzero(pointer, count * size);
	return (pointer);
}
